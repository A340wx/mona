Checkbox::Checkbox(char *label)
{
	this->checked = false;
	this->label  = label;
}

Checkbox::~Checkbox()
{
}

void Checkbox::setChecked(bool checked)
{
	if (this->checked == true && checked == false) {
		this->checked = false;
		repaint();
	} else if (this->checked == false && checked == true) {
		this->checked = true;
		repaint();
	}
}

void Checkbox::setLabel(char *label)
{
	this->label = label;
	repaint();
}

void Checkbox::onPaint(Graphics *g)
{
	int w = getWidth(), h = getHeight();
	
	// 一度背景色でクリア
	g->setColor(getBackground());
	g->fillRect(0, 0, w, h);
	
	// 文字
	//int fw = getFontMetrics()->getWidth(getLabel());
	int fh = getFontMetrics()->getHeight(getLabel());
	//int x = (w - fw) / 2;
	int y = (h - fh) / 2;
	if (getEnabled() == true) {
		g->setColor(getForeground());
	} else {
		g->setColor(Color::GRAY);
	}
	g->drawText(getLabel(), 14/*x*/, y);
	
	// チェック
	if (this->checked == true) {
		for (int i = 0; i < 12; i++) {
			for (int j = 0; j < 12; j++) {
				g->drawPixel(j + 0, i + y, checked_palette[checked_data[i * 12 + j] & 0xFF]);
			}
		}
	} else {
		for (int i = 0; i < 12; i++) {
			for (int j = 0; j < 12; j++) {
				g->drawPixel(j + 0, i + y, unchecked_palette[unchecked_data[i * 12 + j] & 0xFF]);
			}
		}
	}

}

void Checkbox::onEvent(Event *event)
{
	// 非活性の時はイベントを受け付けない
	if (getEnabled() == false) return;

	if (event->getType() == MouseEvent::MOUSE_RELEASED) {
		setChecked(!this->checked);
		event->setType(Event::ITEM_SELECTED);
		getParent()->onEvent(event);
	}
}
